class Solution {
  public:
    double maxVolume(double perimeter, double area) {
       double peri = perimeter;
       double ar = area;
       
       double len = (peri/2 - sqrt(peri * peri/4 - 6*ar))/6;
       
       double ans = len * len * (peri/4-2 * len);
       
       return ans;
    }
};

